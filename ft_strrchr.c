#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*r;

	r = (char *)str;
	while (*str)
		str++;
	while (r != str && (unsigned char)*str != c)
		str--;
	if ((unsigned char)*str == c)
		return ((char *)str);
	return (NULL);
}
